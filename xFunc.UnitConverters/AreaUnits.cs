namespace xFunc.UnitConverters
{
    /// <summary>
    /// Represents area units.
    /// </summary>
    public enum AreaUnits
    {
        /// <summary>
        /// Square millimeters (mm²).
        /// </summary>
        SquareMillimeters,

        /// <summary>
        /// Square centimeters (cm²).
        /// </summary>
        SquareCentimeters,

        /// <summary>
        /// Square metres (m²).
        /// </summary>
        SquareMetres,

        /// <summary>
        /// Square kilometers (km²).
        /// </summary>
        SquareKilometers,

        /// <summary>
        /// Hectares (ha).
        /// </summary>
        Hectares,

        /// <summary>
        /// Square inches (in²).
        /// </summary>
        SquareInches,

        /// <summary>
        /// Square feet (ft²).
        /// </summary>
        SquareFeet,

        /// <summary>
        /// Square yards (yard²).
        /// </summary>
        SquareYards,

        /// <summary>
        /// Acres (ac²).
        /// </summary>
        Acres,

        /// <summary>
        /// Square miles.
        /// </summary>
        SquareMiles
    }
}